public class Cliente {

    private String nome;
    private String cpf;
    
    // get é pegar
    public String getNome() {
        return nome;
    }

    // Set é colocar
    public void setNome(String newNome) {
        this.nome = newNome;
    }

    // get é pegar
    public String getCpf() {
        return cpf;
    }

    // Set é colocar
    public void setCpf(String newCpf) {
        this.cpf = newCpf;
    }
}
